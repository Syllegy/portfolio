/** Deterministic seeded PRNG (mulberry32) — same seed always produces the same sequence, unlike Math.random(), which matters for stable geometry/layout that shouldn't reshuffle on every re-render. */
export function mulberry32(seed: number): () => number {
  return function () {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
